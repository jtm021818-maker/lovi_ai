export function getBondDialogue(spiritId: SpiritId, lv: BondLv): string[] {
  return BOND_DIALOGUES[spiritId]?.[lv] ?? [];
}

/** 랜덤 대사 뽑기 */
export function pickBondDialogue(spiritId: SpiritId, lv: BondLv): string | null {
  const lines = getBondDialogue(spiritId, lv);
  if (lines.length === 0) return null;
  return lines[Math.floor(Math.random() * lines.length)];
}
