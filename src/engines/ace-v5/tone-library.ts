/** 카테고리별 빠른 조회 */
export function getTonesForCategory(category: ToneCategory): ToneExample {
  return TONE_LIBRARY.find(t => t.category === category) ?? TONE_LIBRARY[0];
}

/** 시스템 프롬프트에 삽입할 톤 라이브러리 텍스트 */
export function buildToneLibraryText(): string {
  return TONE_LIBRARY.map(t => {
    const examples = t.examples.map(e => `   "${e}"`).join('\n');
    const desc = t.description ? `\n   → ${t.description}` : '';
    return `### ${t.category}\n   언제: ${t.trigger}${desc}\n${examples}`;
  }).join('\n\n');
}

