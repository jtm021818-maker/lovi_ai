/**
 * Claude에게 실제로 전달할 메시지를 조립
 */
export function buildVoiceUserMessage(params: {
  userUtterance: string;
  brainAnalysis: {
    perceived_emotion: string;
    actual_need: string;
    tone_to_use: string;
    response_length: string;
    draft_utterances: string;
  };
  stakeHint?: string;
}): string {
  const { userUtterance, brainAnalysis, stakeHint } = params;

  const hintBlock = stakeHint
    ? `\n【고위험 힌트】\n${stakeHint}\n`
    : '';

  return `【유저 원문】
"${userUtterance}"

【Gemini 분석 힌트 (참고만, 엇나가면 무시)】
감정: ${brainAnalysis.perceived_emotion}
필요: ${brainAnalysis.actual_need}
톤: ${brainAnalysis.tone_to_use}
길이: ${brainAnalysis.response_length}
초안: ${brainAnalysis.draft_utterances}
${hintBlock}
이제 루나의 말풍선만 출력해. 태그 없이. |||로 구분.`;
}
