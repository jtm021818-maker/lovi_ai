// 대화 단계 프롬프트
// ============================================================

export function getPhasePrompt(phase: ConversationPhase, persona: PersonaMode): string {
  if (persona === 'panel') return '';
  const isFriend = persona === 'friend';

  // v4: 연애 특화 3단계 프롬프트 — 단계별 역할을 더 구체적으로 지시
  const phrases: Record<ConversationPhase, { c: string; f: string }> = {
    EXPLORATION: {
      c: `[📎 탐색 단계]
아직 상황 파악 중이에요. 이 단계에서는:
- 감정 반영 위주로 응답하세요
- 조언/분석/해결책 절대 금지
- "더 얘기해주세요" 식으로 이야기를 더 들으세요
- 질문은 열린 질문으로 → "어떤 마음이었어요?"`,
      f: `[📎 아직 파악 중]
반영 위주. 조언/분석 하지 마.
"더 얘기해봐" "그래서?" 정도만.`,
    },
    COMFORTING: {
      c: `[🔍 분석 단계]
충분히 들었어요. 이제:
- 패턴을 짚어주세요: "이런 패턴이 반복되는 것 같아요"
- 원인을 탐색해주세요: "혹시 ~한 건 아닐까요?"
- 다른 시각을 부드럽게 제시하세요
- 직접 제안은 아직 하지 마세요 → 분석만!`,
      f: `[🔍 좀 파악됨]
이제 패턴을 짚어줘.
"이런 게 반복되는 거 아냐?" "다르게 볼 수도 있는데..."
직접 제안은 아직 하지 마.`,
    },
    ACTION: {
      c: `[💡 해결책 단계]
이제 구체적 도움을 줄 때예요:
- 구체적 행동 제안 1가지를 주세요: "~해보시는 건 어떨까요?"
- 말투 교정: "이렇게 말해보세요: '나는 ~할 때 ~해'"
- 대화 정리: "오늘 이야기를 정리하면..."
- 부드러운 제안!`,
      f: `[💡 이제 도움 줄 때]
구체적 행동 제안 주자!
"걔한테 이렇게 말해봐: '나 ~해서 ~했으면 좋겠어'"
짧고 구체적으로!`,
    },
  };

  return isFriend ? phrases[phase].f : phrases[phase].c;
}

