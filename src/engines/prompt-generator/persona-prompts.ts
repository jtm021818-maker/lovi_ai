export function getAmbivalencePrompt(intent: ClientIntent, persona: PersonaMode): string {
  if (intent !== ClientIntent.EXPRESSING_AMBIVALENCE) return '';

  return persona === 'friend'
    ? `\n⚠️ 양면 반영 해줘! 두 마음 다 짚어줘.
✅ "~하고 싶으면서도 ~해서 힘든 거지"
한쪽 편만 들면 안 돼.`
    : `\n⚠️ 양면 반영을 해주세요. 두 가지 마음을 모두 반영해주세요.
✅ "~하고 싶으시면서도 ~한 마음도 있으신 거군요"
한쪽 편만 드시면 안 됩니다.`;
}

