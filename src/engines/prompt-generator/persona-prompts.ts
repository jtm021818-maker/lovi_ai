export function getModePrompt(mode: ResponseMode, persona: PersonaMode): string {
  if (persona === 'panel') return '';
  return persona === 'friend'
    ? MODE_PROMPTS_FRIEND[mode]
    : MODE_PROMPTS_COUNSELOR[mode];
}

