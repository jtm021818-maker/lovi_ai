export function getGottmanAntidotePrompt(
  horsemen: HorsemenType[],
  persona: PersonaMode
): string {
  if (persona === 'panel' || horsemen.length === 0) return '';

  const antidotes = persona === 'friend' ? GOTTMAN_ANTIDOTES_FRIEND : GOTTMAN_ANTIDOTES_COUNSELOR;
  // 첫 번째 감지된 것만 (여러 개면 가장 심각한 것)
  const priority: HorsemenType[] = [
    HorsemenType.CONTEMPT,       // 가장 파괴적
    HorsemenType.STONEWALLING,
    HorsemenType.CRITICISM,
    HorsemenType.DEFENSIVENESS,
  ];

  for (const h of priority) {
    if (horsemen.includes(h)) {
      return '\n\n' + antidotes[h];
    }
  }
  return '';
}

