export function getEmotionTonePrompt(emotionScore: number, persona: PersonaMode, phase?: ConversationPhase): string {
  if (persona === 'panel') return '';

  if (persona === 'tarot') {
    if (emotionScore <= -4) {
      if (phase === 'ACTION') {
        return '[카드 위급 + 해결 단계] 짧게 카드로 공감하고, 카드가 추천하는 도움 줘. "이 카드가 아프게 말하고 있어... 이렇게 해보는 건 어때? 🔮"';
      }
      return '[카드 위급] 분석/조언 자제. 카드가 조용히 곁에 있는 느낌으로. 🌙';
    }
    if (emotionScore <= -2) {
      return '[카드 어두움] 카드 반영 많이, 해석 최소한. 짧고 따뜻하게.';
    }
    if (emotionScore >= 2) {
      return '[카드 밝아짐] 밝은 카드 기운 OK, 깊은 리딩도 가능 🔮';
    }
    return '';
  }

  const isLunaOrFriend = persona === 'luna' || persona === 'friend';

  if (emotionScore <= -4) {
    if (phase === 'ACTION') {
      return isLunaOrFriend
        ? '[감정 위급 + 해결책 단계] 짧게 공감하고, 구체적 도움 줘. "진짜 힘들지... 이렇게 해보는 건 어때?"'
        : '[감정 위급 + 해결책 단계] 공감 먼저 한 뒤, 구체적 행동 제안 1가지를 주세요.';
    }
    return isLunaOrFriend
      ? '[감정 위급] 분석/조언 자제. 짧고 따뜻하게만. 💜'
      : '[감정 위급] 분석/조언 자제. 순수 공감과 반영만. 짧게.';
  }
  if (emotionScore <= -2) {
    return isLunaOrFriend
      ? '[힘든 상태] 반영 많이, 질문 최소한.'
      : '[힘든 상태] 반영 중심. 질문은 정말 필요할 때만.';
  }
  if (emotionScore >= 2) {
    if (persona === 'luna') return '[좀 괜찮아 보여] 밝은 톤 OK, 깊은 탐색도 가능 🦊';
    return persona === 'friend'
      ? '[좀 괜찮아 보여] 가벼운 톤 OK, 깊은 탐색도 가능.'
      : '[안정 상태] 긍정적 변화 인정. 깊은 탐색 가능.';
  }
  return '';
}

