/**
 * 감정 불확실성이 감지되었을 때 추가되는 사고 힌트.
 * 자율 사고의 확장판 — 상황에 따라 선택적으로 주입.
 */
export function buildEmotionUncertaintyHint(
  emotionScore: number,
  userMessage: string,
): string | null {
  const isAmbiguous = emotionScore >= -2 && emotionScore <= 1;
  const isVague = /힘들|모르겠|그냥|좀|뭔가|약간/.test(userMessage);
  const hasConflict = /화[가나].*슬[퍼프]|슬[퍼프].*화[가나]|모르겠.*뭔지|복잡/.test(userMessage);

  if (hasConflict) {
    return '[참고: 유저 감정이 복잡해 보여. 화난 건지 슬픈 건지 확정하지 않고, 루나라면 자연스럽게 "둘 다인 거 같은데?" 느낌으로 다가갈 수 있어]';
  }
  if (isAmbiguous && isVague) {
    return '[참고: 감정이 아직 애매해. 이름 붙이기보다 좀 더 들어봐. "어떤 느낌이야?" 정도면 충분해]';
  }
  return null;
}

