export function buildUnspokenHint(ctx: {
  userMessage: string;
  sessionTheme: string | null;
  recentUserMessages: string[];
  fears: string[];
  wants: string[];
}): string | null {
  const parts: string[] = [];

  // 유저가 반복적으로 드러내는 두려움/욕구가 있으면 맥락으로 제공
  if (ctx.fears.length > 0) {
    parts.push(`유저가 두려워하는 것: ${ctx.fears.join(', ')}`);
  }
  if (ctx.wants.length > 0) {
    parts.push(`유저가 원하는 것: ${ctx.wants.join(', ')}`);
  }

  if (parts.length === 0) return null;

  return `[유저 내면 추정 — 확신 없으면 건드리지 마]\n${parts.join('\n')}`;
}
