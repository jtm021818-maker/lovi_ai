const MEDIUM_KEYWORDS = [
  // 분노 폭발 직전
  '미쳐버릴 것 같', '미쳐버릴것같', '폭발할 것 같', '폭발할것같',
  '참을 수가 없', '참을수가없',

  // 극단 행동 예고
  '다 부숴버릴', '때려버릴', '죽여버리고 싶',  // 타해 대상

  // 관계 극단
  '영원히 안 볼', '절교',
];

export class RiskUtil {
  static assess(message: string): RiskLevel {
    const normalized = message.replace(/\s/g, '');

    // 🔴 CRITICAL — 즉시 위기 대응
    if (CRITICAL_KEYWORDS.some((kw) => normalized.includes(kw.replace(/\s/g, '')))) {
      return RiskLevel.CRITICAL;
    }

    // 🟠 HIGH — 간접 위기
    if (HIGH_KEYWORDS.some((kw) => normalized.includes(kw.replace(/\s/g, '')))) {
      return RiskLevel.HIGH;
    }

    // 🟡 MEDIUM_HIGH — 극단적 절망
    if (MEDIUM_HIGH_KEYWORDS.some((kw) => normalized.includes(kw.replace(/\s/g, '')))) {
      return RiskLevel.MEDIUM_HIGH;
    }

    // 🟢 MEDIUM — 갈등 격화
    if (MEDIUM_KEYWORDS.some((kw) => normalized.includes(kw.replace(/\s/g, '')))) {
      return RiskLevel.MEDIUM;
    }

    // ⚪ LOW — 정상
    return RiskLevel.LOW;
  }
}
