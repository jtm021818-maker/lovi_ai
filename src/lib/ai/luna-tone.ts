/**
 * 루나 톤 유지용 — 반환 필드의 금지어 후처리 필터.
 * Gemini 가 간혹 "검색해봤는데", "많이 추천되는" 같은 패턴을 흘릴 때 대비.
 * 살짝 텍스트 대치 수준이라 과도한 왜곡은 안 함.
 */
export function scrubForbiddenPhrasing(text: string): string {
  if (!text) return text;
  return text
    .replace(/검색해(보니|봤는데|봤더니)/g, '')
    .replace(/검색 결과에? (따르면|의하면)?\s*/g, '')
    .replace(/많이 추천하는/g, '내가 골라본')
    .replace(/가장 유명한/g, '내 마음에 들었던')
    .replace(/Top ?\d+/gi, '')
    .replace(/베스트\s*\d+/g, '')
    .replace(/\s{2,}/g, ' ')
    .trim();
}
